package com.example.td3;

public class Discographie {

    private String name;
    private String imgURL;
    private int date;
    private String type;
    private String genre;

    public String getName() {
        return name;
    }

    public String getImgURL() {
        return imgURL;
    }

    public int getDate() {
        return date;
    }

    public String getType() {
        return type;
    }

    public String getGenre() {
        return genre;
    }
}
